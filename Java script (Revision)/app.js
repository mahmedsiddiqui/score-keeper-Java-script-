function inc() {
  

    let first = document.getElementById("one")


    first.innerText = parseInt(first.innerText) + 1

}

function dec() {
    
    
    let sec = document.getElementById("one")


    sec.innerText = parseInt(sec.innerText) - 1
    
    


}

